export async function seedCatalogLocations() {
  console.log("Checking catalog_locations for existing data...");

  const [result] = await db.select({ count: count() }).from(catalogLocations);
  if (result.count > 0) {
    console.log(`Catalog locations already has ${result.count} entries, skipping seed.`);
    return;
  }

  console.log(`Seeding ${allLocations.length} catalog locations...`);
  await db.insert(catalogLocations).values(allLocations as any);
  console.log(`Successfully seeded ${allLocations.length} catalog locations.`);
}

export default seedCatalogLocations;
